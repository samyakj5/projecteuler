# we know that odd numbers cannot be prime-generating, 
# because for any odd number 2k + 1, 1 + (2k + 1)/1 = 2k + 2
# which is divisible by 2.

# any 2+ digit number ending with 4 or 6 also cannot be prime-generating:
# if we have ...6, then 2 + ...6/2 = 2 + ...3 = ...5, divisible by 5
# if we have ...4, then 1 + ...4/1 = ...5, divisible by 5

# so we only have to consider numbers ending with 0, 2, or 8.